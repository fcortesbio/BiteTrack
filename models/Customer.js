const mongoose = require('mongoose');

const customerSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
    trim: true
  },
  lastName: {
    type: String,
    required: true,
    trim: true
  },
  phoneNumber: {
    type: String,
    required: true,
    trim: true
  },
  email: {
    type: String,
    sparse: true, // Allows multiple null values but unique non-null values
    lowercase: true,
    trim: true
  },
  lastTransaction: {
    type: Date,
    default: null
  }
}, {
  timestamps: true
});

// Transform output
customerSchema.set('toJSON', {
  transform: function(doc, ret) {
    ret.id = ret._id;
    delete ret._id;
    delete ret.__v;
    return ret;
  }
});

module.exports = mongoose.model('Customer', customerSchema);